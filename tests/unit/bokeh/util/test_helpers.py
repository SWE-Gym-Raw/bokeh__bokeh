def test_flatten() -> None:
    assert buh.flatten([]) == []
    assert buh.flatten([[1, 2, 3], [4, 5], [6], []]) == [1, 2, 3, 4, 5, 6]

def test_flat_map() -> None:
    assert buh.flat_map(lambda n: ["a"]*n, [1, 2, 3]) == ["a", "a", "a", "a", "a", "a"]

